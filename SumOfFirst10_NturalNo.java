public class SumOfFirst10_NturalNo {
	public static void main(String[] args)   
	{  
	int sum = 0;  
	for(int i = 1; i <= 10; i++) //loop for condition until true 
	{   
	sum = sum + i;  
	}                                                                   
	System.out.println("Sum of First 10 Natural Numbers is = " + sum);   //prints the sum 
	}  
	}
